        dummy = ListNode()
        current = dummy
        
        # Traverse both lists and merge
        while l1 and l2:
            if l1.val <= l2.val:
                current.next = l1
                l1 = l1.next
            else:
                current.next = l2
                l2 = l2.next
            current = current.next
        
